package model;

/**
 *
 * @author jr972
 */
public class Sala {
    
    private int id;
    
    private int num;

    private int capacidad;

    public Sala(int id, int num, int capacidad) {
        this.id = id;
        this.num = num;
        this.capacidad = capacidad;
    }

    public Sala() {
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getNum() {
        return num;
    }

    public void setNum(int num) {
        this.num = num;
    }

    public int getCapacidad() {
        return capacidad;
    }

    public void setCapacidad(int capacidad) {
        this.capacidad = capacidad;
    }
    
    
    
    
    
}
